# Function Documentation
# Docstrings: a string that appears at the beginning of a function
# that describes what the function does

def add_ten(num):
    """This function adds 10 to a number
    INPUTArgs:
        num: a number
    Returns:
        the number plus 10
    """
    return(num + 10)

def add_ten_default(num, defaddend=10):
    """This function adds a default value to a number"""
    return(num + defaddend)

def add_ten_keyword(num, defaddend=10):
    """This function adds a default value to a number"""
    return(num + defaddend)

