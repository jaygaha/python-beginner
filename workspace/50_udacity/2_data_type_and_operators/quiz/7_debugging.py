# Debugging
#
# Common errors:
#
# * Syntax error
# * NameError
# * TypeError
# * IndexError
# * KeyError
# * ValueError
# * AttributeError
#
# How to debug:
#
# * Read the error message
# * Use the interpreter to find the line of code that caused the error
# * Use the interpreter to find the value of variables at that point
# * Use the interpreter to find the type of the value of a variable
