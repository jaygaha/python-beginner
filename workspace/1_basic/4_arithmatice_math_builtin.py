# Buit in math module

x = 3.14
y = 4
z = 5
a = -3.14

# Round: rounding off the number
result = round(x)

print(result) # 3

# Abs: absolute value: distance from 0 and the number itself
result = abs(a)

print(result) # 3.14

# Pow: power
result = pow(4, 3) # 4^3

print(result) # 64

# Max: maximum value
result = max(x, y, z)

print(result) # 5

# Min: minimum value

result = min(x, y, z)

print(result) # 3.14
