# Add
balls = 0

#balls = balls + 1
balls += 1 # Augmented Assignment Operator

print(balls)

# Subtract
friends = 0

#friends = friends - 1
friends -= 1

print(friends)

# Multiply
tuffy = 2

#tuffy = tuffy * 2
tuffy *= 2

print(tuffy)

# Divide
pizza = 8

#pizza = pizza / 2
pizza /= 2

print(pizza)

# Exponent: Power

# 2^3
fish = 2
print(fish ** 3) # 8

fish **= 2
print(fish) # 4

# Modulus: Remainder

# 5 % 2
print(5 % 2) # 1

pizza = 10
remainder = pizza % 3

